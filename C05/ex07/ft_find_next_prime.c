int		ft_find_next_prime(int nb)
{
	int k;
	int l;

	l = 2;
	k = 0;
	if (nb < 2)
		return (2);
	while (l <= (nb + k) / l)
	{
		if (((nb + k) % l) != 0)
			l++;
		else
		{
			l = 2;
			k++;
		}
	}
	if (l > (nb + k) / l)
		return (nb + k);
	return (0);
}
