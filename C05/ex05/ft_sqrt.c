int		ft_sqrt(int nb)
{
	int k;

	k = 0;
	if (nb >= 0 && nb <= 2147483647)
	{
		while ((k * k <= nb))
		{
			if (k * k == nb)
				return (k);
			else
				k++;
		}
		return (0);
	}
	return (0);
}

int main()
{
	printf("%d", ft_sqrt(2147395600));
}
