#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int k;
	int l;

	k = 0;
	while (k < size - 1)
	{
		if (tab[k] > tab[k + 1])
		{
			l = tab[k];
			tab[k] = tab[k + 1];
			tab[k + 1] = l;
			k = -1;
		}
		k++;
	}
}
