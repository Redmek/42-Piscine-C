#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int k;

	k = 0;
	while (str[k])
	{
		if (str[k] >= 'A' && str[k] <= 'Z')
			str[k] += 32;
		else
			k++;
	}
	return (str);
}
