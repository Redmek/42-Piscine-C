#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int k;

	k = 0;
	while (str[k])
	{
		if (str[k] >= 'a' && str[k] <= 'z')
			str[k] -= 32;
		else
			k++;
	}
	return (str);
}
