#include <unistd.h>

int		main(int ac, char **av)
{
	int k;
	int l;

	k = ac - 1;
	l = 0;
	while (k > 0)
	{
		l = 0;
		while (av[k][l] != '\0')
		{
			write(1, &av[k][l], 1);
			l++;
		}
		write(1, "\n", 1);
		k--;
	}
	return (0);
}
