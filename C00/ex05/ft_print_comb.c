#include <unistd.h>

void	ft_print_comb(void)
{
	char k;
	char l;
	char i;

	k = '0';
	while (k <= '7')
	{
		l = k + 1;
		while (l <= '8')
		{
			i = l + 1;
			while (i <= '9')
			{
				write(1, &k, 1);
				write(1, &l, 1);
				write(1, &i, 1);
				if (k != '7')
					write(1, ", ", 2);
				i++;
			}
			l++;
		}
		k++;
	}
}
