#include <unistd.h>

int		ft_strcmp(char *s1, char *s2)
{
	int k;
	int l;

	k = 0;
	l = 0;
	while ((s1[k] != '\0' || s2[k] != '\0') && l == 0)
	{
		if (s1[k] != s2[k])
			l = (s1[k] - s2[k]);
		k++;
	}
	return (l);
}
